package Day1.entity;

public class Customer1 {
	//declare variables as public
	private int cid;
	private String name;
	private String city;
	//using get and set method
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}

}